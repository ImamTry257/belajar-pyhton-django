a = 8
b = 3

# ada + , - , / , *


# eksponen
hasil = a ** b # a pangkat b
print(a,"**",b,"=",hasil)

# modulus
hasil = a % b # hasilnya a dibagi b kemudian sisanya berapa
print(a,"%",b,"=",hasil)

# floor division
hasil = a // b # hasilnya a dibagi b dengan pembulatan ke bawah
print(a,"//",b,"=",hasil)