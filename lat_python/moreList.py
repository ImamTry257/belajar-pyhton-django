data = ["pisang", "mangga", "anggur"]

print(data)

# tambah data di akhir array
data.append("lemon")
print(data)

# tambah data di posisi sesuai keinginan
data.insert(2, "singkong")
print(data)


# cara menghitung banyaknya data dengan data value
jumlahData = data.count("singkong")
print(jumlahData)

# reverse
data.reverse()
print(data)
